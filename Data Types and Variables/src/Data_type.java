
public class Data_type {

	public static void main(String[] args) {
		
		//byte
		byte b1=25;
		byte b2=-128;
		byte b3=127;
		System.out.println(b1 + "|" + b2 + "|" + b3 );
//		byte b4 = 128; 
		
		System.out.println("-----------");
		
		// short
		short s1=500;
		short s2=-23768;
		short s3=32767;
//		short s4=32768;     
		System.out.println(s1 + "|" + s2 + "|" + s3 );
		
		System.out.println("-----------");
		
		// int
		int i1 = -5000;
		int i2 = 100_000;
		int i3 = 50000;
		System.out.println(i1 + "|" + i2 + "|" + i3);
		
		System.out.println("-----------");
		
		//long
		long l1 = 100_000_000;
		long l2 = 241255474555L;
		
		System.out.println(l1 + "|" + l2);
		
		System.out.println("-----------");
		
		//float:floating point types have double as defaulf type 
		float f1 = 5.2f;
		double d1 = 5.2;
		
//		System.out.println(f1 + "|" + d2);
		
		System.out.println("-----------");
	
		// char 
		char c1 = 'D';
		char c2 = '$';
//		char star = '&#42';
		
		System.out.println(c1 + "|" + c2);
		
//		System.out.println(f1 + "|" + d2);
		

	}

}
